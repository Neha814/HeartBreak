public class DateUtils {
 
 /*
 -------------------------------------------------------------------------------------------------------------------------------------------------------------
 | Symbol | Meaning       | Type   | Type                    |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | G  | Era        | Text   | GG -> AD                   |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | y  | Year       | Number   | yy -> 03, yyyy -> 2003                |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | M  | Month       | Text or Number | M -> 7 , M -> 12 , MM -> 07 , MMM -> Jul , MMMM -> December       |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | d  | Day in month     | Number   | d -> 3 , dd -> 03                  |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | h  | Hour (1-12, AM/PM)    | Number   | h -> 3 ,  hh  ->  03                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | H  | Hour (0-23)      | Number   | H -> 15 , HH -> 15                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | k  | Hour (1-24)      | Number   | k -> 3 , kk -> 03                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | K  | Hour (0-11 AM/PM)    | Number   | K -> 15 , KK -> 15                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | m  | Minute       | Number   | m -> 7 , m -> 15 , mm -> 15               |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | s  | Second       | Number   | s -> 15 , ss -> 15                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | S  | Millisecond (0-999)    | Number   | SSS -> 007                   |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | E  | Day in week      | Text   | EEE -> Tue , EEEE -> Tuesday              |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | D  | Day in year (1-365 or 1-364) | Number   | D -> 65 , DDD -> 065                |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | F  | Day of week in month (1-5)  | Number   | F -> 1                    |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | w  | Week in year (1-53)    | Number   | w -> 7                    |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | a  | AM/PM       | Text   | a -> AM , aa -> AM                 |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 | z  | Time zone      | Text   | z -> EST , zzz -> EST , zzzz -> Eastern Standard Time        |
 |------------------------------------------------------------------------------------------------------------------------------------------------------------
 
 */
 
 public static String YYYY_MM_DD = "yyyy-MM-dd H:m";
 public static String FORMATED_SHOUT_TIME = "dd MMM, yyyy hh:mm aa";
 
 public static String getCurrentTimeStr(){
  Date currentDate = new Date();
  TimeZone tz = TimeZone.getTimeZone("UTC");
  SimpleDateFormat sdf = new SimpleDateFormat(YYYY_MM_DD);
  sdf.setTimeZone(tz);
  String curentDate = sdf.format(currentDate);
  return curentDate;
 }
 
 public static String getConvertedLocalTimeStr(String sentUtcTime){
  String result = "";
  SimpleDateFormat sourceFormat = new SimpleDateFormat(YYYY_MM_DD);
  sourceFormat.setTimeZone(TimeZone.getTimeZone("UTC"));
  SimpleDateFormat format = new SimpleDateFormat(YYYY_MM_DD);
  
  try {
   Date parsed = sourceFormat.parse(sentUtcTime);
   TimeZone tz = TimeZone.getTimeZone(TimeZone.getDefault().getID());
   SimpleDateFormat destFormat = new SimpleDateFormat(YYYY_MM_DD);
   destFormat.setTimeZone(tz);
   result = destFormat.format(parsed);
  } catch (ParseException e) {
   // TODO Auto-generated catch block
   e.printStackTrace();
  }
  return result;
 }
 
}